import './bootstrap';

import Alpine from 'alpinejs';

window.Alpine = Alpine;

Alpine.start();

const eyeIcon = '<svg xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z"></path><circle cx="12" cy="12" r="3"></circle></svg>';
const eyeOffIcon = '<svg xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M17.94 17.94A10.07 10.07 0 0 1 12 20c-7 0-11-8-11-8a18.45 18.45 0 0 1 5.06-5.94M9.9 4.24A9.12 9.12 0 0 1 12 4c7 0 11 8 11 8a18.5 18.5 0 0 1-2.16 3.19m-6.72-1.07a3 3 0 1 1-4.24-4.24"></path><line x1="1" y1="1" x2="23" y2="23"></line></svg>';

const updatePasswordToggleIcon = (button, isVisible) => {
	button.innerHTML = isVisible ? eyeOffIcon : eyeIcon;
	button.setAttribute('aria-label', isVisible ? 'Hide password' : 'Show password');
	button.setAttribute('title', isVisible ? 'Hide password' : 'Show password');
};

const initializePasswordToggles = (root = document) => {
	const passwordInputs = root.querySelectorAll('input[type="password"]:not([data-password-toggle-ready])');

	passwordInputs.forEach((input) => {
		input.setAttribute('data-password-toggle-ready', 'true');

		const parent = input.parentElement;
		if (!parent) {
			return;
		}

		// Wrap the input in its own relative container
		const wrapper = document.createElement('div');
		wrapper.className = 'relative';
		parent.insertBefore(wrapper, input);
		wrapper.appendChild(input);

		input.classList.add('pr-10');

		const button = document.createElement('button');
		button.type = 'button';
		button.className = 'absolute right-3 top-1/2 -translate-y-1/2 text-gray-400 hover:text-gray-600 dark:text-gray-500 dark:hover:text-gray-300 focus:outline-none';

		updatePasswordToggleIcon(button, false);

		button.addEventListener('click', () => {
			const isHidden = input.type === 'password';
			input.type = isHidden ? 'text' : 'password';
			updatePasswordToggleIcon(button, isHidden);
		});

		wrapper.appendChild(button);
	});
};

document.addEventListener('DOMContentLoaded', () => {
	initializePasswordToggles();
});
